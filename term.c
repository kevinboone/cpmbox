#include "term.h"

/*===========================================================================

  tm_size

  Return the size of the terminal, or -1,-1 if it is non-sized.
  TODO: at present, we only support an 80x24 terminal.

===========================================================================*/
void tm_size (rows, cols)
int *rows;
int *cols;
  {
  *rows = 24;
  *cols = 80;
  }

/*===========================================================================

  tm_g_rchar

===========================================================================*/
int tm_g_rchar ()
  {
  return bios (3, 0, 0);
  }


